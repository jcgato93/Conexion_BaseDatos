
package Controlador;


public class Productos {
    
    private String nArticulo;
    private String nSeccion;
    private String nPrecio;
    private String pOrigen;

    public String getnArticulo() {
        return nArticulo;
    }

    public void setnArticulo(String nArticulo) {
        this.nArticulo = nArticulo;
    }

    public String getnSeccion() {
        return nSeccion;
    }

    public void setnSeccion(String nSeccion) {
        this.nSeccion = nSeccion;
    }

    public String getnPrecio() {
        return nPrecio;
    }

    public void setnPrecio(String nPrecio) {
        this.nPrecio = nPrecio;
    }

    public String getpOrigen() {
        return pOrigen;
    }

    public void setpOrigen(String pOrigen) {
        this.pOrigen = pOrigen;
    }
    
    
}
